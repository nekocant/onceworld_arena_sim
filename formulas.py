# -----------------------------------------------------------------------
# 1. レベル倍率（全ステータス共通）
# -----------------------------------------------------------------------
# 計算式: lv_multiplier = LV_BASE + (level - 1) * LV_SCALE
# 例: Lv.100 → 1 + (100 - 1) * 0.1 = 10.9倍
LV_BASE = 1       # レベル1の時の基礎倍率
LV_SCALE = 0.1    # レベルが1上がるごとの増加量


# -----------------------------------------------------------------------
# 2. HP計算
# -----------------------------------------------------------------------
# 计算式: HP = math.floor(VIT * lv_multiplier) * HP_VIT_COEFFICIENT + HP_BASE
# 例: VIT=3, Lv.100 → math.floor(3 * 10.9) = 32 → 32 * 18 + 100 = 676
HP_VIT_COEFFICIENT = 18   # VIT 1ポイントあたりのHP変換係数
HP_BASE = 100             # 全モンスター共通の基礎HP


# -----------------------------------------------------------------------
# 3. 防御力計算（DEF / MDEF のクロス補正）
# -----------------------------------------------------------------------
# 計算式:
#   base_def  = math.floor(CSV_DEF  * lv_multiplier)
#   base_mdef = math.floor(CSV_MDEF * lv_multiplier)
#   最終DEF  = math.floor(base_def  + base_mdef * DEF_CROSS_RATIO)
#   最終MDEF = math.floor(base_mdef + base_def  * MDEF_CROSS_RATIO)
#
# つまり物理防御にも魔法防御の一部が加算され、逆も同様。
DEF_CROSS_RATIO = 0.1    # MDEFからDEFへの補正割合 (10%)
MDEF_CROSS_RATIO = 0.1   # DEFからMDEFへの補正割合 (10%)


# -----------------------------------------------------------------------
# 4. 移動速度
# -----------------------------------------------------------------------
# 計算式: speed = MOV_BASE_SPEED * (1 + MOV * MOV_SCALE_PER_POINT)
# MOV=0のモンスターは MOV_ZERO_SPEED で固定（完全静止ではない）
MOV_BASE_SPEED = 80.0          # 移動速度の基本値（ピクセル/秒）
MOV_SCALE_PER_POINT = 0.10     # MOV 1ポイントあたりの速度増加率 (10%)
MOV_ZERO_SPEED = 10.0          # MOV=0 の場合の最低移動速度


# -----------------------------------------------------------------------
# 5. 攻撃速度（SPD → 秒間攻撃回数の変換テーブル）
# -----------------------------------------------------------------------
# SPD値と秒間攻撃回数の対応表（区間ごとに線形補間）
# (SPD値, 秒間攻撃回数)
ATK_SPEED_TABLE = [
    (0,    1.0),
    (100,  1.5),
    (200,  2.0),
    (300,  2.5),
    (400,  3.0),
    (500,  3.5),
    (600,  4.0),
    (700,  4.5),
    (800,  5.0),
    (3000, 20.0),
]

# SPD >= 3000 の超高速域の追加倍率
ATK_SPEED_ULTRA_TIERS = [
    # (SPD閾値, 倍率)  ※ 上から順に判定、最初に一致したものを使用
    (100000, 5.0),
    (30000,  4.0),
    (10000,  3.0),
    (3001,   2.0),
]
ATK_SPEED_ULTRA_BASE = 20.0  # 超高速域のベース攻撃回数

# アニメーション用：攻撃モーション間隔の上限（これより遅くならない）
ATK_INTERVAL_MAX = 0.25  # 秒


# -----------------------------------------------------------------------
# 6. 攻撃射程
# -----------------------------------------------------------------------
RANGE_MELEE = 30.0     # 近接の射程距離（ピクセル）
RANGE_RANGED = 150.0   # 遠隔の射程距離（ピクセル）


# -----------------------------------------------------------------------
# 7. ダメージ計算
# -----------------------------------------------------------------------
# 計算式:
#   物理: base_dmg = (ATK * DMG_STAT_MULTIPLIER) - 相手DEF
#   魔法: base_dmg = (INT * DMG_STAT_MULTIPLIER) - 相手MDEF
#   → 負の場合は 0 に切り上げ
#   → base_dmg *= DMG_FINAL_MULTIPLIER
#   → RNG乱数 (DMG_RNG_MIN ~ DMG_RNG_MAX)
#   → 最終ダメージが 0以下 → 1に底上げ
DMG_STAT_MULTIPLIER = 1.75   # ATK/INTに掛ける倍率
DMG_FINAL_MULTIPLIER = 4.0   # 基礎ダメージに掛ける全体倍率
DMG_RNG_MIN = 0.9            # ダメージ乱数の下限
DMG_RNG_MAX = 1.1            # ダメージ乱数の上限
DMG_MINIMUM = 1              # 最低保証ダメージ


# -----------------------------------------------------------------------
# 8. 属性相性（倍率テーブル）
# -----------------------------------------------------------------------
# {攻撃属性: {相手属性: 倍率}} （記載のない組み合わせは 1.0倍＝等倍）
ELEMENT_TABLE = {
    '火': {'木': 1.2, '水': 0.8},
    '水': {'火': 1.2, '木': 0.8},
    '木': {'水': 1.2, '火': 0.8},
    '光': {'闇': 1.2, '光': 0.8},
    '闇': {'光': 1.2, '闇': 0.8},
}
ELEMENT_DEFAULT = 1.0  # テーブルにない組み合わせのデフォルト倍率


# -----------------------------------------------------------------------
# 9. クリティカル
# -----------------------------------------------------------------------
# 発動条件: 自分と相手のLUCK差の比率が CRIT_LUCK_THRESHOLD 以内
# 計算式: luck_diff_ratio = |自LUCK - 相手LUCK| / max(自LUCK, 相手LUCK)
CRIT_LUCK_THRESHOLD = 0.2  # LUCK差20%以内で発動判定に入る
CRIT_CHANCE = 0.05          # クリティカル発生確率 (5%)
CRIT_MULTIPLIER = 2.5       # クリティカルダメージ倍率


